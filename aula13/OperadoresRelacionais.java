package luciano.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int valor1=1;
		int valor2 = 2; 
		
		System.out.println("Valor 1 igual ou valor 2 ? " + (valor1==valor2));
		System.out.println("Valor 1 diferente ou valor 2 ? " + (valor1!=valor2));
		System.out.println("Valor 1 maior ou valor 2 ? " + (valor1>valor2));
		System.out.println("Valor 1 maior ou igual ao valor 2 ? " + (valor1>=valor2));
		System.out.println("Valor 1 menor ao valor 2 ? " + (valor1<valor2));
		System.out.println("Valor 1 menor ou igual ao valor 2 ? " + (valor1<=valor2));
	}

}
