package luciano.cursojava.aula13.labs;

public class Exerc14 {

	public static void main(String[] args) {
		int resultado = 1;
		System.out.println(resultado);
		resultado++;
		System.out.println(resultado);
		System.out.println(resultado++);
		System.out.println(++resultado);
		resultado--;
		System.out.println(resultado);
		System.out.println(resultado--);
		System.out.println(--resultado);

	}

}
